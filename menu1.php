                    // Implementasi overloading
                    public function testOverloading($param1, $param2 = null) {
                        if ($param2 === null) {
                            echo "Metode kelebihan beban dengan satu argumen:  " . $param1;
                        } else {
                            echo "Metode kelebihan beban dengan satu argumen: : " . $param1 . " and " . $param2;
                        }
                    }
                }

                class Motor extends Kendaraan {
                    private $kapasitasMesin;

                    public function __construct($merk, $tahun, $kapasitasMesin) {
                        parent::__construct($merk, $tahun);
                        $this->kapasitasMesin = $kapasitasMesin;
                    }

                    // Overriding
                    public function tampilkanInfo() {
                        echo "<strong>Motor:</strong> " . $this->getMerk() . ", <strong>Kapasitas Mesin:</strong> " . $this->kapasitasMesin . " cc, <strong>Tahun:</strong> " . $this->getTahun();
                    }
                }

                // Membuat objek dari kelas Mobil dan Motor
